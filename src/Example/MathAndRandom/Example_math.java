package Example.MathAndRandom;

public class Example_math {
    public static void main(String[] args) {
        System.out.println("绝对值："+Math.abs(-9));
        System.out.println("计算平方根："+Math.sqrt(4));
        System.out.println("计算立方根："+Math.cbrt(9));
        System.out.println("计算乘方："+Math.pow(2,3));
        System.out.println("参数的最小整数："+Math.ceil(4.7));
        System.out.println("四舍五入："+Math.round(-3.5));
        System.out.println("生成一个大于等于0.0小于1的随机数："+Math.random());
    }
}
